    private void botaoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEntrarActionPerformed
        String senha = senhaField.getText();
        String cpf = cpfField.getText();
        UsuarioDAO dao = new UsuarioDAO();
        int idUsuario = dao.verificarLogin(senha, cpf);

if (idUsuario != -1) {
    SessaoUsuario.setId(idUsuario);

    if (cpf.equalsIgnoreCase("adm")) {
        
        this.setVisible(false);
        TelaAdmin admin = new TelaAdmin(idUsuario);
        admin.setVisible(true);
    } else {
        
        CartaoDAO cartaoDAO = new CartaoDAO();
        if (!cartaoDAO.cartaoExiste(idUsuario)) {
            String numeroGerado = cartaoDAO.gerarNumeroCartao(idUsuario);
            cartaoDAO.inserirCartao(numeroGerado, idUsuario);
        }
        this.setVisible(false);
        TelaEscolha t3 = new TelaEscolha();
        t3.setVisible(true);
    }
} else {
    JOptionPane.showMessageDialog(this, "Senha ou CPF incorretos.");
}
    }//GEN-LAST:event_botaoEntrarActionPerformed

